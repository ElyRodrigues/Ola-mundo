#Escreva um programa que leia um valor em metros e o exiba covertido em centímetros e milímetros.vars
num1 = float(input('Entre com uma unidade em metros para que seja convertida pra centímetros e milimetros: '))
num2 = num1 * 100
num3 = num1 * 1000
print(f'{num1} metros, equivale a {num2} centímetros e {num3} milímetros.') 